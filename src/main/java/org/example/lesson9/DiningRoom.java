package org.example.lesson9;

public class DiningRoom {

    private int maxCapacity;
    private int maxDish;

    public DiningRoom(int maxCapacity) {
        this.maxCapacity = maxCapacity;
    }

    public int getMaxCapacity() {
        return maxCapacity;
    }

    public int getMaxDish() {
        return maxDish;
    }
}
